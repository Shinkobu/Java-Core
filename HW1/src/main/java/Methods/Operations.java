package Methods;

public class Operations {
    public int sum (int a, int b) {
        return a+b;

    }
    public int multi (int a, int b) {
        return a*b;

    }
    public int division (int a, int b) {
        return a/b;

    }
    public int reduction (int a, int b) {
        return a-b;

    }

}
