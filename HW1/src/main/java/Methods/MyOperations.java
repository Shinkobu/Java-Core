package Methods;

public class MyOperations {
    public static int sum(int a, int b) {
        return a+b;

    }
    public static int multi (int a, int b) {
        return a*b;

    }
    public static int division (int a, int b) {
        return a/b;

    }
    public static int reduction (int a, int b) {
        return a-b;

    }

}
